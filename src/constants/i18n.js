const resolveTranslation = (dictionary, keyPath) => {
  const keys = keyPath.split('.');
  let value = dictionary;
  for (const key of keys) {
    value = value?.[key];
  }
  return value;
};

export const getTranslation = (language, keyPath) => {
  const dictionary = translations[language] || translations[DEFAULT_LANGUAGE];
  const fallbackDictionary = translations[DEFAULT_LANGUAGE];

  const aliasKeyPath = keyPath.startsWith('term.')
    ? keyPath.replace(/^term\./, 'terms.')
    : keyPath.startsWith('terms.')
      ? keyPath.replace(/^terms\./, 'term.')
      : null;

  const value = resolveTranslation(dictionary, keyPath)
    ?? (aliasKeyPath ? resolveTranslation(dictionary, aliasKeyPath) : undefined)
    ?? resolveTranslation(fallbackDictionary, keyPath)
    ?? (aliasKeyPath ? resolveTranslation(fallbackDictionary, aliasKeyPath) : undefined);

  return value ?? keyPath;
};
